# filter просто фильтрует набор с помощью функции, которую мы ей передали
print(list(filter((lambda x: x % 2 == 0), list(range(10)))))

######################################################################################################################

from functools import reduce

# reduce - агрегирует данные, с помощью переданной функции
print(reduce((lambda x, y: x + y), [1, 2, 3, 4]))
